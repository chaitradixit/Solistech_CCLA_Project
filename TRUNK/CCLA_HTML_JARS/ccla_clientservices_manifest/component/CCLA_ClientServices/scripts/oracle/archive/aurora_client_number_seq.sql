
-- Create new sequence used for Aurora Client Number assignment.
CREATE SEQUENCE CCLA.SEQ_AURORA_CLIENTNUMBER MINVALUE 66000 MAXVALUE 70000 INCREMENT BY 1 START WITH 66000 CACHE 20 NOORDER NOCYCLE;

CREATE OR REPLACE PUBLIC SYNONYM SEQ_AURORA_CLIENTNUMBER FOR CCLA.SEQ_AURORA_CLIENTNUMBER;
GRANT ALL ON SEQ_AURORA_CLIENTNUMBER TO UCMADMIN;