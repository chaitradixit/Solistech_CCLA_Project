/

-- Add keys for table IRMODULE_INSTR_RULE_APPLIED

ALTER TABLE IRMODULE_INSTR_RULE_APPLIED ADD CONSTRAINT INSTRRULEAPPL_PK PRIMARY KEY (IRMODULE_INSTR_RULE_ID)
